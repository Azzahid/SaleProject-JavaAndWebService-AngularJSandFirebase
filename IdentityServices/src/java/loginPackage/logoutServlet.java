    public void deleteTokenDB(String token, String username) throws SQLException{
        try {
            Connection con = DB.connect();
            Timestamp now = new Timestamp(new Date().getTime());
            Statement newPS = con.createStatement();
            newPS.executeUpdate("UPDATE user SET Token='0' ,createAt='"+now+"' WHERE username ='"+username+"' AND Token='"+token+"'");
            con.close();
        } catch(Exception e) {
            System.err.println("Got an login exception!");
            System.err.println(e.getMessage());
        }
    }
